// Current language
let currentLanguage = localStorage.getItem('kaamconnect_lang') || 'en';

// Change language function
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('kaamconnect_lang', lang);
    updatePageTranslations();
    closeLanguageModal();
    
    // Update chatbot language if exists
    if (window.chatbot) {
        window.chatbot.updateLanguage(lang);
    }
    
    // Announce change for accessibility
    announceToScreenReader(`Language changed to ${lang}`);
}

// Update all translations on page
function updatePageTranslations() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            const translation = translations[currentLanguage][key];
            
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage;
}

// Open/Close language modal
function openLanguageModal() {
    const modal = document.getElementById('languageModal');
    if (modal) {
        modal.classList.add('active');
        document.body.style.overflow = 'hidden';
    }
}

function closeLanguageModal() {
    const modal = document.getElementById('languageModal');
    if (modal) {
        modal.classList.remove('active');
        document.body.style.overflow = '';
    }
}

// Accessibility helper
function announceToScreenReader(message) {
    const announcement = document.createElement('div');
    announcement.setAttribute('role', 'status');
    announcement.setAttribute('aria-live', 'polite');
    announcement.className = 'sr-only';
    announcement.style.cssText = 'position:absolute;left:-10000px;width:1px;height:1px;overflow:hidden;';
    announcement.textContent = message;
    document.body.appendChild(announcement);
    
    setTimeout(() => {
        document.body.removeChild(announcement);
    }, 1000);
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    updatePageTranslations();
});

// Export for global access
window.translations = translations;
